unsigned char* invertirImagen(const unsigned char* bits, int size) {
    // Crear un nuevo array dinámicamente
    unsigned char* imagenInvertida = (unsigned char*)malloc(size * sizeof(unsigned char));
    
    if (imagenInvertida == NULL) {
        printf("Error al asignar memoria\n");
        return NULL;  // Retornar NULL si falla la asignación de memoria
    }

    // Invertir los bits de cada byte
    for (int i = 0; i < size; i++) {
        imagenInvertida[i] = ~bits[i];  // Invertir cada byte usando NOT bit a bit
    }

    return imagenInvertida;  // Retornar el nuevo array
}
